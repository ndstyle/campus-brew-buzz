// Fallback coordinates for center of US
export const fallbackCoordinates = {
  lat: 39.8283,
  lng: -98.5795,
  zoom: 4
};

export const getSchoolCoordinates = (schoolName) => {
  if (!schoolName) return fallbackCoordinates;
  
  // Try exact match first
  if (schoolCoordinates[schoolName]) {
    return schoolCoordinates[schoolName];
  }
  
  // Try partial match (case insensitive)
  const normalizedSchool = schoolName.toLowerCase();
  for (const [key, coords] of Object.entries(schoolCoordinates)) {
    if (key.toLowerCase().includes(normalizedSchool) || 
        normalizedSchool.includes(key.toLowerCase())) {
      return coords;
    }
  }
  
  return fallbackCoordinates;
};